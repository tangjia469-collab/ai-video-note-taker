function svg(pathContent, size = 18, stroke = 2) {
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="${stroke}" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${pathContent}</svg>`;
}

export function icon(name, size = 18, stroke = 2) {
  const p = ICON_PATHS[name];
  if (!p) return '';
  return svg(p, size, stroke);
}

// 给已有元素填入图标(支持 data-icon 属性自动渲染)
export function renderIcon(el, name, size = 18, stroke = 2) {
  if (!el) return;
  el.innerHTML = icon(name, size, stroke);
}

// 自动渲染页面里所有 [data-icon] 元素
export function autoRenderIcons(root = document) {
  root.querySelectorAll('[data-icon]').forEach((el) => {
    const name = el.getAttribute('data-icon');
    const size = parseInt(el.getAttribute('data-icon-size') || '18', 10);
    const stroke = parseFloat(el.getAttribute('data-icon-stroke') || '2');
    renderIcon(el, name, size, stroke);
  });
}
